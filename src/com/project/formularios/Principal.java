    public void buscador() {

        String buscador2 = (String) comboBuscador.getSelectedItem();
        String buscador = buscadorTxt.getText();
        boolean buscador1 = buscadorTxt.getText().isEmpty();
        Integer item = comboBuscador.getSelectedIndex();

        if (buscador1 != true) {

            comboBuscador.removeAllItems();
            cargarNombresEspecificos(buscador, item);
        }
        if (miModeloNombre.getSize() < 1) {
            comboBuscador.removeAllItems();
            cargarNombres();
        } else {
            comboBuscador.removeAllItems();
            cargarNombres();
        }

        if (buscador1) {

            if (item > 0) {
                comboBuscador.removeAllItems();
                cargarNombresEspecificos(buscador2, item);
            } else {
                comboBuscador.removeAllItems();
                cargarNombres();
            }

        }

        if (miModeloNombre.getSize() == 1) {
            miModeloNombre.removeAllElements();
            cargarNombres();

        }

    }

