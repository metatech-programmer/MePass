    private void AceptarPanelAdd() {

        String nombreWebTexto, urlTexto, nombreUsuarioTexto, contrasennaTexto, correoCifrado, claveCifrada;
        correoCifrado = recibirCorreo.getText();
        claveCifrada = recibirContrasena.getText();
        nombreWebTexto = cajitaNombreWeb2.getText();
        urlTexto = cajitaURL2.getText();
        nombreUsuarioTexto = cajitaNombreUsuario2.getText();
        contrasennaTexto = String.valueOf(cajitaContrasena2.getPassword());

        if (nombreWebTexto.isEmpty() || urlTexto.isEmpty() || nombreUsuarioTexto.isEmpty() || contrasennaTexto.isEmpty()) {
            JOptionPane.showMessageDialog(panelPopap, "Completa todos los campos de información", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {

            ContrasenasGuardadasDAO objContraGuardDAO = new ContrasenasGuardadasDAO();

            Acceso objAcces = new Acceso(null, correoCifrado, claveCifrada);

            ContrasenasGuardadas objContraGuard = new ContrasenasGuardadas(0, objAcces, nombreWebTexto, urlTexto, nombreUsuarioTexto, contrasennaTexto);

            if (objContraGuardDAO.agregar(objContraGuard)) {
                borrar();
                lblAgregar.setText("Contraseña agregada correctamente");
                lblAgregar.requestFocus();

            } else {
                JOptionPane.showMessageDialog(panelPopap, "Vuelve a revisar los datos", "Advertencia", JOptionPane.WARNING_MESSAGE);

            }
        }
    }

