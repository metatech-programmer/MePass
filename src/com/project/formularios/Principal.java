    private void cargarNombresEspecificos(String buscador, int index) {

        int indice = 0;
        String cadena = "  ";
        List<ContrasenasGuardadas> miArregloContrasenas;
        ContrasenasGuardadas objetito = new ContrasenasGuardadas();

        String correo = recibirCorreo.getText();
        String contrasenna = recibirContrasena.getText();

        String[] nombreSeleccionado = new String[10];
        String[] cadenitaUrl = new String[10];
        String[] cadenitaaNombre = new String[10];
        String[] cadenaContrasenna = new String[10];
        ContrasenasGuardadasDAO miDaoContrasenas = new ContrasenasGuardadasDAO();

        miModeloNombre.addElement(cadena);
        codigoNombre.put(0, 0);

        miArregloContrasenas = miDaoContrasenas.listadito2("", buscador, correo, contrasenna);
        for (ContrasenasGuardadas objeto : miArregloContrasenas) {
            indice++;
            miModeloNombre.addElement(objeto.getNombrePagina());
            codigoNombre.put(indice, objeto.getCodContraGuard());
            nombreSeleccionado[indice] = objeto.getNombrePagina();
            cadenitaUrl[indice] = objeto.getUrl();
            cadenitaaNombre[indice] = objeto.getNombreUsuario();
            cadenaContrasenna[indice] = objeto.getContrasenasGuardadas();

        }
        if (miModeloNombre.getSize() == 1) {
            miModeloNombre.removeAllElements();
            cargarNombres();
            lblname.setText("No existe ninguna contraseña");
            cajitaURL.setText("");
            cajitaNombredeUsuario.setText("");
            cajitaContrasena.setText("");
        } else {

            comboBuscador.setModel(miModeloNombre);
            lblname.setText(nombreSeleccionado[1]);
            cajitaURL.setText(cadenitaUrl[1]);
            cajitaNombredeUsuario.setText(cadenitaaNombre[1]);
            cajitaContrasena.setText(cadenaContrasenna[1]);
            lblUrl.setText(cadenitaUrl[1]);
            lblNombreUsu.setText(cadenitaaNombre[1]);
            lblContrasenna.setText(cadenaContrasenna[1]);

            miModeloNombre.removeAllElements();
            cargarNombres();
        }
    }

