    private boolean correoValidoMano(String quieroValidarlo) {
        boolean bandera;
        Matcher analizar = correoValido.matcher(quieroValidarlo);
        bandera = analizar.matches();
        return bandera;
    }

    private void VerificarCajaCorreo(JTextField correo, JPasswordField contrasena) {
        if (correo.getText().equals(textoCorreo)) {
            correo.setText("");
            correo.setForeground(new Color(255, 255, 255));

        }
        if (String.valueOf(contrasena.getPassword()).isEmpty()) {
            contrasena.setText(contraseña);
            contrasena.setForeground(new Color(134, 247, 233));
        }
    }

    private void VerificarCajaContrasena(JTextField correo, JPasswordField contrasena) {
        if (String.valueOf(contrasena.getPassword()).equals(contraseña)) {
            contrasena.setText("");
            contrasena.setForeground(new Color(255, 255, 255));
        }
        if (correo.getText().isEmpty()) {
            correo.setText(textoCorreo);
            correo.setForeground(new Color(134, 247, 233));
        }
    }

    private void VerificarRegistro(JTextField Principal, JTextField otro1, JTextField otro2) {
        if (Principal.getText().equals(textoDocumento) || Principal.getText().equals(textoNombres)
                || Principal.getText().equals(textoApellidos)) {
            Principal.setText("");
            Principal.setForeground(new Color(255, 255, 255));
        }

        if (otro1.getText().isEmpty() && otro2.getText().isEmpty()) {
            if (otro1 == cajitaNombres && otro2 == cajitaApellidos) {
                otro1.setText(textoNombres);
                otro2.setText(textoApellidos);

            }
            if (otro1 == cajitaDocumento && otro2 == cajitaApellidos) {
                otro1.setText(textoDocumento);
                otro2.setText(textoApellidos);

            }
            if (otro1 == cajitaDocumento && otro2 == cajitaNombres) {
                otro1.setText(textoDocumento);
                otro2.setText(textoNombres);

            }

            otro1.setForeground(new Color(134, 247, 233));
            otro2.setForeground(new Color(134, 247, 233));

        } else if (otro1.getText().isEmpty()) {
            if (otro1 == cajitaNombres) {

                otro1.setText(textoNombres);
            }
            if (otro1 == cajitaDocumento) {

                otro1.setText(textoDocumento);
            }

            otro1.setForeground(new Color(134, 247, 233));

        } else if (otro2.getText().isEmpty()) {
            if (otro2 == cajitaNombres) {

                otro2.setText(textoNombres);
            }
            if (otro2 == cajitaApellidos) {

                otro2.setText(textoApellidos);
            }

            otro2.setForeground(new Color(134, 247, 233));
        }
    }

    private void VerificarYaAcabe(JTextField correo, JPasswordField contrasena, JPanel entrada) {
        try {
            if (correo.getText().equals("")) {
                correo.setText(textoCorreo);
                correo.setForeground(new Color(134, 247, 233));
                entrada.requestFocus();

            }

            if (String.valueOf(cajitaContrasena.getPassword()).equals("")) {
                contrasena.setText(contraseña);
                contrasena.setForeground(new Color(134, 247, 233));
                entrada.requestFocus();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(entrada, "Completa todos los campos de información", "Advertencia", JOptionPane.WARNING_MESSAGE);

        }

    }

