    private void AceptarPanel2(JTextField correo, JPasswordField contrasena, JPanel entrada) {

        String correoTexto, contrasenaTexto;

        VerificarYaAcabe(correo, contrasena, entrada);

        contrasenaTexto = String.valueOf(contrasena.getPassword());
        correoTexto = correo.getText();

        if (correo.getText().equals(textoCorreo) || contrasenaTexto.equals(contraseña) || contrasenaTexto.isEmpty()) {
            JOptionPane.showMessageDialog(entrada, "Completa todos los campos de información", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {

            if (correoValidoMano(cajitaCorreo.getText()) || correo == cajitaCorreo) {
                String claveCifrada = Seguridad.obtenerHash(contrasenaTexto, misConstantes.MI_PIZCA_DE_CONTRASENNA);
                String correoCifrado = Seguridad.obtenerHash(correoTexto, misConstantes.MI_PIZCA_DE_CORREO);
                Acceso objAcceso = new Acceso();
                AccesosDAO objAccesDao = new AccesosDAO();

                objAcceso.setCorreoAcceso(correoCifrado);
                objAcceso.setClaveAcceso(claveCifrada);

                if (objAccesDao.verificarUsuarios("", objAcceso)) {

                    Principal princi = new Principal();

                    princi.setVisible(true);
                    Principal.recibirContrasena.setText(claveCifrada);
                    Principal.recibirCorreo.setText(correoCifrado);
                    this.dispose();

                } else {
                    JOptionPane.showMessageDialog(panelPrincipal, "Datos incorrectos, intentalo nuevamente", "Advertencia", JOptionPane.WARNING_MESSAGE);
                }
            }
        }
    }

    private void AceptarPanel(JTextField correo, JPasswordField contrasena, JPanel entrada) {

        String correoTexto, contrasenaTexto;

        VerificarYaAcabe(correo, contrasena, entrada);

        contrasenaTexto = String.valueOf(contrasena.getPassword());
        correoTexto = correo.getText();

        if (correoValidoMano(cajitaCorreo2.getText()) && correo == cajitaCorreo2) {

            String documentoUsuario, nombresUsuario, apellidosUsuario;
            documentoUsuario = cajitaDocumento.getText();
            nombresUsuario = cajitaNombres.getText();
            apellidosUsuario = cajitaApellidos.getText();

            String claveCifrada = Seguridad.obtenerHash(contrasenaTexto, misConstantes.MI_PIZCA_DE_CONTRASENNA);
            String correoCifrado = Seguridad.obtenerHash(correoTexto, misConstantes.MI_PIZCA_DE_CORREO);
            String documentoCifrado = Seguridad.obtenerHash(documentoUsuario, misConstantes.MI_PIZCA_DE_DOCUMENTO);
            String nombreCifrado = Seguridad.obtenerHash(nombresUsuario, misConstantes.MI_PIZCA_DE_NOMBRES);
            String apellidoCifrado = Seguridad.obtenerHash(apellidosUsuario, misConstantes.MI_PIZCA_DE_APELLIDOS);

            AccesosDAO objDAOAcces = new AccesosDAO();
            Usuarios miUser = new Usuarios(0, documentoCifrado, nombreCifrado, apellidoCifrado, null);
            Acceso objAcces = new Acceso(miUser, correoCifrado, claveCifrada);

            if (objDAOAcces.agregar(objAcces)) {
                registrarse();

            }
        } else {
            JOptionPane.showMessageDialog(panelPrincipal, "Datos incorrectos, intentalo nuevamente", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }

    }

